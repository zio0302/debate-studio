/**
 * 토론 컨텍스트를 포함한 사용자 메시지를 구성하는 헬퍼
 */
export function buildDebateContext(params: {
  rawInput: string;
  additionalConstraints?: string | null;
  previousMessages: { speaker: string; content: string }[];
  currentTask: string;
  outputTone: string;
}): string {
  const { rawInput, additionalConstraints, previousMessages, currentTask, outputTone } = params;

  // 결과 톤에 따른 지시
  const toneInstruction = {
    concise: "응답은 간결하게, 각 항목당 1~2줄로 제한하세요.",
    standard: "응답은 표준 길이로, 각 항목당 2~3줄로 작성하세요.",
    detailed: "응답은 상세하게, 충분한 설명과 예시를 포함하세요.",
  }[outputTone] ?? "응답은 표준 길이로 작성하세요.";

  // 이전 토론 내용 조합
  const debateHistory =
    previousMessages.length > 0
      ? `\n\n## 지금까지의 토론 내용\n${previousMessages
          .map((m) => `**[${m.speaker}]**\n${m.content}`)
          .join("\n\n---\n\n")}`
      : "";

  return `## 검토 대상 기획안
${rawInput}

${additionalConstraints ? `## 추가 조건 및 제약\n${additionalConstraints}` : ""}
${debateHistory}

## 현재 요청
${currentTask}

## 출력 지시
${toneInstruction}
반드시 지정된 출력 형식을 준수하세요.`;
}
